package bean;

public class AnimalType {
    private int animalTypeId;
    private String animalTypeName;
    
    public AnimalType() {}
    
    public int getAnimalTypeId() { return animalTypeId; }
    public void setAnimalTypeId(int animalTypeId) { this.animalTypeId = animalTypeId; }
    
    public String getAnimalTypeName() { return animalTypeName; }
    public void setAnimalTypeName(String animalTypeName) { this.animalTypeName = animalTypeName; }
}
